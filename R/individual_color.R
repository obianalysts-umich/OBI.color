#' Individual OBI Color
#' @description All OBI colors
#' @param show_color if display the color
#'
#'
#' @return
#' @export
#'
#' @examples

# Primary ---------------------------------------------------------
#'
#' @rdname Primary
prim_dark_blue <- function(show_color = F) {
  val = "#1e3668"

  # display color
  if(show_color){scales::show_col(val)}

  return(val)
}

